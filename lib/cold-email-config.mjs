export function buildSubject(p, config) {
  const key = !p.has_site
    ? 'subject_no_site'
    : p.has_site && p.https === false && config.subject_no_https
      ? 'subject_no_https'
      : 'subject'

  return (config[key] || config.subject)
    .replaceAll('{first_name}', p.first_name)
    .replaceAll('{city}', p.city)
    .replaceAll('{practice_area}', p.practice_area || '')
}

export function buildBody(p, config) {
  const key = !p.has_site
    ? 'body_no_site'
    : p.has_site && p.https === false && config.body_no_https
      ? 'body_no_https'
      : 'body'

  return config[key]
    .map(line => line
      .replaceAll('{first_name}', p.first_name)
      .replaceAll('{city}', p.city)
      .replaceAll('{practice_area}', p.practice_area || '')
      .replaceAll('{firm_site}', p.firm_site || 'votre site'),
    )
    .join('\n')
}

